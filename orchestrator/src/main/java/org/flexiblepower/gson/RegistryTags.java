package org.flexiblepower.gson;

public class RegistryTags {
	private String name;
	private String[] tags;
	
	public RegistryTags(){}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String[] getTags() {
		return tags;
	}
	public void setTags(String[] tags) {
		this.tags = tags;
	}
	
	
}
